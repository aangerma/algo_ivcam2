#ifndef CYNW_TLM_UTILS_HEADER
#define CYNW_TLM_UTILS_HEADER


#define CYNW_ABS(x) \
 ( (x) > 0 ? (x) : (-(x)))

#define CYNW_GET_NUM_BITS(x) \
 ( ((x) & 0x80000000) ? 32 \
 : ((x) & 0x40000000) ? 31 \
 : ((x) & 0x20000000) ? 30 \
 : ((x) & 0x10000000) ? 29 \
 : ((x) & 0x08000000) ? 28 \
 : ((x) & 0x04000000) ? 27 \
 : ((x) & 0x02000000) ? 26 \
 : ((x) & 0x01000000) ? 25 \
 : ((x) & 0x00800000) ? 24 \
 : ((x) & 0x00400000) ? 23 \
 : ((x) & 0x00200000) ? 22 \
 : ((x) & 0x00100000) ? 21 \
 : ((x) & 0x00080000) ? 20 \
 : ((x) & 0x00040000) ? 19 \
 : ((x) & 0x00020000) ? 18 \
 : ((x) & 0x00010000) ? 17 \
 : ((x) & 0x00008000) ? 16 \
 : ((x) & 0x00004000) ? 15 \
 : ((x) & 0x00002000) ? 14 \
 : ((x) & 0x00001000) ? 13 \
 : ((x) & 0x00000800) ? 12 \
 : ((x) & 0x00000400) ? 11 \
 : ((x) & 0x00000200) ? 10 \
 : ((x) & 0x00000100) ? 9 \
 : ((x) & 0x00000080) ? 8 \
 : ((x) & 0x00000040) ? 7 \
 : ((x) & 0x00000020) ? 6 \
 : ((x) & 0x00000010) ? 5 \
 : ((x) & 0x00000008) ? 4 \
 : ((x) & 0x00000004) ? 3 \
 : ((x) & 0x00000002) ? 2 \
 : 1 )

#endif
